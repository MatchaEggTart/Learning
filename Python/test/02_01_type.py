# 整数
print("整数")
print(0b11)    		# 二进制整数
print(0o11) 		# 八进制整数
print(11)		# 十进制整数
print(0x11)		# 十六进制整数
print("\n")

# 浮点数
print("浮点数")
print(123.456)		# 数学计数法
print(1.23456e2)	# 科学计数法
print("\n")

# 字符串
print("字符串")
print('Hello, World')
print("Hello, World")
print("\n")

# Boolean
print("布尔值")
print(True)
print(False)
print(1 == 2)
print("\n")
